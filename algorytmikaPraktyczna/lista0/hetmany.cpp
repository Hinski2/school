constexpr int mod = 1e9 + 7;
constexpr int inf = 1e9 + 7;
constexpr int mak = 13;

int ans = 0, n;
bool board[mak][mak];
bool colum[mak];
bool up[3 * mak], down[3 * mak];

inline bool ok(int r, int c){
    return board[r][c] == 0 and colum[c] == 0 and up[r + c] == 0 and down[r + n - c] == 0;
}

void hetman(int r){
    if(r == n){
        ans++;
        return;
    }

    for(int c = 0; c < n; c++){
        if(!ok(r, c)) continue;

        board[r][c] = 1;
        colum[c] = 1;
        up[r + c] = 1;
        down[r + n - c] = 1;

        hetman(r + 1);

        board[r][c] = 0;
        colum[c] = 0;
        up[r + c] = 0;
        down[r + n - c] = 0;
    }
}

